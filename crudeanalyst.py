# -----------------------------
# Crack Spread Calculator
# -----------------------------
st.subheader("üßÆ Refining Margin (Crack Spread) Calculator")

colC, colD, colE = st.columns(3)

with colC:
    crude_price = st.number_input("Crude Price (USD/bbl)", value=80.0)
with colD:
    gasoline_price = st.number_input("Gasoline Price (USD/bbl)", value=95.0)
with colE:
    diesel_price = st.number_input("Diesel Price (USD/bbl)", value=100.0)

crack_spread = (0.5 * gasoline_price + 0.5 * diesel_price) - crude_price

st.metric("3-2-1 Crack Spread", f"${crack_spread:.2f}")

# -----------------------------
# News Feed
# -----------------------------
st.subheader("üì∞ Latest Oil Market News")

def get_news():
    url = "https://newsapi.org/v2/everything"
    params = {
        "q": "crude oil OR OPEC OR energy markets",
        "sortBy": "publishedAt",
        "language": "en",
        "apiKey": "YOUR_NEWS_API_KEY"
    }
    try:
        r = requests.get(url, params=params)
        data = r.json()
        return data.get("articles", [])
    except:
        return []

articles = get_news()

for a in articles[:5]:
    st.write(f"### {a['title']}")
    st.write(a['description'])
    st.write(f"[Read more]({a['url']})")
    st.write("---")